package com.week1;

//public 접근제어자(public : 접근에 제한이 없음)
public class Test1 {
	
	//main method(반드시 있어야 하는 실행 명령)
	//static을 주면 메모리에 알아서 올라가있다.
	//void는 실행하고 돌려주는 값이 없다.
	//메소드이름은 소문자 + 영문자로 시작한다.
	//(String[] args) : 인수, 매개변수
	public static void main(String[] args) {
		 
		System.out.println("첫번째 프로그램\n");
		System.out.println("두번째 프로그램");
	}

}